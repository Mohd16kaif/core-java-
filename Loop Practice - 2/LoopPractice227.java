public class LoopPractice227{


public static void main (String[] args){

for(int i=1; i<=5;i++){
	for(int j=5;j>=i;j--){
		System.out.print("*");
		}
	for(int l=1;l<=5;l++){
for(int m=1;m<=l;m++){
System.out.print(" ");
}
for(int n=i;n<=5;n++){
System.out.print("*");
}

System.out.println();
	
	
}

}
}
}

